// A factory method is a method that returns an object. Generally used when you have
// 1 class or interface that can have multiple implementation. But instead of using the new keyword to create the object, you use a factory method.
// A static method that creates an object that is inside the class
// When ou have mutliple implementation of an interface or class using the constructor is either not always possible or not always the best solution
// (because the parameters name might not be descriptive). The factory method solves this problem, by having the necessary parameters.
// The downside is that the different object implementation have to share exactly the same interface.
