// A factory class is a class that contains multiple factory methods. Instead of having the factory method inside the class that it creates, you have a separate class that contains the factory methods.
// You do that when you want to separate the creation of the object from the object itself and make the class only have 1 responsibility.
// Single responsibility principle: a class should have only 1 reason to change.
// The factory class is a class that has the responsibility to create the object.
// The object itself has the responsibility to do what it is supposed to do.
